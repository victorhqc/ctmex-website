// ¿Qué es CTMEX?
//
// Colegio de tenis mexicano es un nuevo sistema educativo deportivo aplicado al tenis, donde el alumno aprende de forma divertida, didáctica y profesional.
//
// ¿Cómo funciona?
//
// Funciona por medio de objetivos sencillos a corto plazo y también por evaluaciones cada determinado tiempo. Así teniendo un sistema de medición para conocer el desarrollo del alumno y descubrir sus fortalezas y debilidades.
//
// ¿A quién va dirigido?
//
// El colegio inicia con niños y niñas a partir de 4 a 7 años de edad, esto pensado en el mejor desarrollo físico, mental, social y tenístico para el alumno.
//
// ¿En qué consta el programa?
//
// El programa consta en que el alumno aprenda y comprenda pequeñas dosis de instrucciones técnicas así el o ella podrá ir entendiendo y empezando jugar este deporte sin limitantes.
//
// ¿Cómo son sus niveles o años escolares?
//
// Se conforma de 3 etapas, 2 años cada etapa.
// Al ir cambiando de etapa, la bola, la cancha y la raqueta cambian o crecen conforme el alumno también lo hace, esto pensado y estudiado para que el alumno le facilite el aprendizaje y desarrollo del mismo.
//
// ¿Cómo es cada etapa del ciclo escolar?
//
// Estas 3 etapas se distinguen por su diferencia de tamaño de cancha, tamaño y peso de raqueta, tamaño, peso y velocidad de pelota.
// Conforme el alumno pasa de etapa a etapa, aumenta el tamaño, peso y velocidad de pelota, cancha y raqueta al igual que su desarrollo físico, mental, social y tenístico.
//
// En pocas palabras... “los alumnos no crecen con el tenis, el tenis crece con los alumnos”.
//
// ¿Cuáles son los costos y qué incluye?
//
// El precio de la colegiatura mensual y de inscripción no está autorizado en decirlo, con una previa cita a las instalaciones se les podrá brindar una mejor información y aclaración de cualquier tipo de dudas que puedan tener.
//
// La inscripción incluye.-
//
// Dos playeras tipo polo (uniforme)
// Seguro en caso de algún accidente dentro de las instalaciones.
//
// Las clases son de lunes a viernes con duración de 90 minutos conformados de las siguiente manera: 45 minutos de entrenamiento de tenis
//
// 45 minutos de desarrollo físico
//
// También se cuenta con un área de espera didáctica para los alumnos y un espacio especial para los padres que gusten esperar a sus hijos.
//
// Horarios.-
//
// 4:00 a 5:30 pm 5:00 a 6:30 pm 6:00 a 7:30 pm 7:00 a 8:30 pm
//
// ¿Porqué elegir CTMEX?
//
// ·Clases de lunes a viernes
// ·No suspendemos clases por aire o lluvia (canchas techadas)
// ·No es necesaria una acción o membresía como en un club privado ·Canchas especializadas para la edad y tamaño del alumno ·Pelotas especiales para la edad y tamaño del alumno
// ·Área de espera didáctica
// ·Desarrollo físico desde los 4 años
// ·Sistema de medición para saber el progreso del alumno
// ·Atención enfocada en pequeños de 4 a 7 años
